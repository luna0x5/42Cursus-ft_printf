#include "ft_printf.h"

int	ft_putuns(unsigned int n)
{
	int	i;

	i = 0;
	if (n <= 9)
		i += ft_putchar(n + '0');
	else
	{
		i += ft_putuns(n / 10);
		i += ft_putchar(n % 10 + '0');
	}
	return (i);
}
